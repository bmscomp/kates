func TestRenderMarkdownReport_Basic(t *testing.T) {
	report := &client.Report{
		Summary: &client.ReportSummary{
			TotalRecords:            100000,
			AvgThroughputRecPerSec:  50000,
			PeakThroughputRecPerSec: 75000,
			AvgThroughputMBPerSec:   48.8,
			AvgLatencyMs:            5.2,
			P50LatencyMs:            4.1,
			P95LatencyMs:            12.3,
			P99LatencyMs:            25.7,
			MaxLatencyMs:            120.0,
			ErrorRate:               0.0001,
		},
	}

	md := renderMarkdownReport("test-123", report)

	expectations := []string{
		"# Kates Performance Report",
		"**Test ID**: `test-123`",
		"## Throughput",
		"100.0K",
		"## Latency Distribution",
		"P99",
		"25.70",
		"## Reliability",
		"0.0100%",
	}
	for _, exp := range expectations {
		if !strings.Contains(md, exp) {
			t.Errorf("markdown report missing: %s", exp)
		}
	}
}

func TestRenderMarkdownReport_WithSLA(t *testing.T) {
	report := &client.Report{
		Summary: &client.ReportSummary{TotalRecords: 1000},
		OverallSlaVerdict: &client.SlaVerdict{
			Passed: false,
			Violations: []client.SlaViolation{
				{Metric: "p99LatencyMs", Threshold: 50, Actual: 120},
			},
		},
	}

	md := renderMarkdownReport("sla-test", report)

	if !strings.Contains(md, "SLA violations detected") {
		t.Error("expected SLA violations text")
	}
	if !strings.Contains(md, "p99LatencyMs") {
		t.Error("expected violation metric name")
	}
}

